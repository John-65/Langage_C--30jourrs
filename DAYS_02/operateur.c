/*Nous avons les opérateur arithmétique, logique et relationnel
les Opérateurs arithmétiques: +, -, *, /, %
Les opérateurs relationnels : <, >, ==, <=, >=, !=
Les opérateurs logiques: &&, !

ces opérateurs nous permettent de faire des opérations, de faire la comparaison

*/
// un petit exemple avec les opérateurs arithmétique:

#include <stdio.h>

int main()
{
    int a = 5;
    int b = 2;
    printf("Somme : %d\n", a + b);
    printf("Produit : %d\n", a * b);
    printf("Difference : %d\n", a - b);
    printf("Division : %d\n", a / b);

    return 0;
}